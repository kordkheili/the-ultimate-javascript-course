const myFriends = ["Mostafa", "Hesam"];
console.log(myFriends);

// ! Add Elements to Array

// push()
myFriends.push("Arman");
console.log(myFriends);

// unshift()
myFriends.unshift("Behnam");
console.log(myFriends);

// ! Remove Elements from Array

// pop()
myFriends.pop();
console.log(myFriends);

// shift()
myFriends.shift();
console.log(myFriends);
